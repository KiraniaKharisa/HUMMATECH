// Buatlah variabel numerik dengan menyesuaikan nominal isiannya.

// Berat badan: 56 kg
// Tinggi badan: 160 cm
// Uang di dompet senilai: Rp. 30.000
// Uang di rekening: Rp. 2.100.000.000
// Nilai investasi: Rp 30.000.000.000
// Jarak sekolah ke rumah: 5.5 km

public class Tugas2 {
    public static void main(String[] args) {
        double beratBadan = 56;
        System.out.println("Berat Badan : " + beratBadan + " KG ");
        
        float tinggiBadan = 1.55f;
        System.out.println("Tinggi Badan : " + tinggiBadan + " Meter ");
        
        int uangDiDompet = 30000;
        System.out.println("Uang Di Dompet Senilai : " + "Rp. " + uangDiDompet );

        int uangDiRekening = 2100000000;
        System.out.println("Uang Di Rekening : " + "Rp. " + uangDiRekening );

        long nilaiInvestasi = 30000000000L;
        System.out.println("Nilai Investasi : " + "Rp. " + nilaiInvestasi );
        
        float jarak = 5.5f;
        System.out.println("Jarak sekolah ke rumah : " + jarak + " km");
    }
}
