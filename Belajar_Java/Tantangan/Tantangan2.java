//Buatlah 5 variabel (int, boolean, double, char)
//nama variabel tidak boleh sama

public class Tantangan2 {
    public static void main(String[] args) {
        int jarak = 100;
        System.out.println("Jarak saya berlari = " + jarak + " KM");
        //isinya berupa angka 0 - 9 contohnya 123, tidak boleh decimal (angka menggunakan koma) contohnya 12,3

        boolean laptop = true;
        System.out.println("Apakah ada laptop? : " + laptop + " (ada)");
        //isinya hanya true (Benar) atau false (Salah), menunjukkan benar atau salah

        double phi = 3.14;
        System.out.println("Berapakah angka phi? : " + phi);
        //Tipe data untuk menyimpan nilai bilangan pecahan

        char bajuAnak = 'S';
        System.out.println("Berapa ukuran baju anak? : " + bajuAnak);
        //isinya berupa satu huruf atau character, dikutip dengan '', contohnya 'a'

        char bajuRemaja = 'L';
        System.out.println("Berapa ukuran baju remaja? : " + bajuRemaja);
        //isinya berupa satu huruf atau character, dikutip dengan '', contohnya 'a'
    }
}
